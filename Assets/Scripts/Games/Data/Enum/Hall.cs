namespace Games.Data
{
    /// <summary>
    /// Represents the different halls where machines can be located.
    /// </summary>
    public enum Hall
    {
        /// <summary>
        /// The hall designated for beginners.
        /// </summary>
        Beginner = 1,

        /// <summary>
        /// The general hall for regular players.
        /// </summary>
        General = 2,

        /// <summary>
        /// The hall for experienced players and masters.
        /// </summary>
        Master = 3,

        /// <summary>
        /// The hall for honored or distinguished players.
        /// </summary>
        Honor = 4,
    }
}